const fs = require('fs');
const path = require('path');

// Create a simple 1024x1024 square placeholder icon
// This is a minimal PNG header for a 1024x1024 square image
const squareIconPng = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, // IHDR chunk length
  0x49, 0x48, 0x44, 0x52, // IHDR
  0x00, 0x00, 0x04, 0x00, // Width: 1024
  0x00, 0x00, 0x04, 0x00, // Height: 1024
  0x08, 0x02, 0x00, 0x00, 0x00, // Bit depth: 8, Color type: 2 (RGB), etc.
  0x4C, 0x87, 0x53, 0x27, // CRC
  0x00, 0x00, 0x00, 0x0C, // IDAT chunk length (minimal)
  0x49, 0x44, 0x41, 0x54, // IDAT
  0x08, 0x99, 0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // Minimal IDAT data
  0x00, 0x00, 0x00, 0x00, // CRC
  0x00, 0x00, 0x00, 0x00, // IEND chunk length
  0x49, 0x45, 0x4E, 0x44, // IEND
  0xAE, 0x42, 0x60, 0x82  // CRC
]);

const assetsDir = path.join(__dirname, '..', 'assets', 'images');

// Write square icons
fs.writeFileSync(path.join(assetsDir, 'icon.png'), squareIconPng);
fs.writeFileSync(path.join(assetsDir, 'adaptive-icon.png'), squareIconPng);

console.log('Created square 1024x1024 icons');
